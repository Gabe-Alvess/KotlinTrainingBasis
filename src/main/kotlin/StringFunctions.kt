fun main() {
    // Some String Functions

    var newName = "Gabriel Alves" // This variable is an object/class of type String | (Same logic as in Java)
    var nameLength = newName.length // Property of a String object
    println("Name length: $nameLength") // Kotlin template concatenation
    var upperCase = newName.uppercase() // Function or method that changes a String object behavior
    println(upperCase)
    var lowerCase = newName.lowercase()
    println(lowerCase)
}